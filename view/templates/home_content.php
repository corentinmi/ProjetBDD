				<h2 class="title"><a href="#">The DBLP Computer Science Bibliography</a></h2>
				<p class="meta"><span class="date">29 Avril 2013</span><span class="posted"></span></p>
				<div style="clear: both;">&nbsp;</div>
				<div class="entry">
					<p>On vous demande de créer une application de consultation de références en utilisant les données fournies par The DBLP Computer Science Bibliography</p>
					<p>L'application stocke la description d'un ensemble d'articles, de livres, de théses de master ou de doctorat.
					Pour chaque source, le système retiendra l'auteur, l'éditeur, le titre, l'année, le journal, l'url, l'isbn, le chapitrele "publisher".</p>
					<p>Le système acceptera deux types d'utilisateurs : les utilisateurs anonymes et les administrateurs. Les
					utilisateurs anonymes auront uniquement le droit de consultation. Les administrateurs auront en plus les
					droit d'insertion, de suppression et de modification. Les administateurs auront un identifiant (leur adresse
					email) ainsi qu'un mot de passe hachée(á l'aide par exemple de SHA-256) et pourront s'authentifier sur le
					système.</p>
					<p>Tous les utilisateurs devront pouvoir consulter facilement toutes les informations présentes dans la base de
					données. Notamment, ils devront pouvoir :
					->Consulter toutes les informations d'un article, d'un livre ou d'une thèse.
					->Rechercher des articles, des livres ou des thèses sur base de leur nom, de leur année de publication, des
					auteurs, l'isbn,...
					->Consulter toutes les informations d'un auteur ainsi que toutes les publications auxquelles il a contribué.</p>
					
